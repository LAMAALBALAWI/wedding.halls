    <h2 style="color:#d63384;">الشروط والأحكام</h2>

    <p>
        مرحبًا بك في منصة حجز القاعات. قبل استخدامك لخدمات الموقع، يرجى قراءة الشروط والأحكام التالية بعناية.
        دخولك أو استخدامك للموقع يعني موافقتك الكاملة على هذه الشروط.
    </p>

    <h3>1. مسؤولية المستخدم</h3>
    <p>
        - يجب على المستخدم إدخال بيانات صحيحة عند إنشاء الحساب أو عند الحجز.<br>
        - أي محاولة لتقديم معلومات مضللة قد تؤدي إلى إيقاف الحساب.
    </p>

    <h3>2. سياسة الحجز</h3>
    <p>
        - يتم إرسال طلب الحجز إلى مالك القاعة للموافقة أو الرفض.<br>
        - لا يعتبر الحجز مؤكدًا إلا بعد موافقة مالك القاعة.<br>
        - يحق لمالك القاعة قبول أو رفض أي طلب دون إبداء الأسباب.
    </p>

    <h3>3. نسبة الموقع (5٪)</h3>
    <p>
        - يحق لإدارة الموقع الحصول على نسبة <strong>5٪</strong> من قيمة أي حجز يتم تأكيده عبر المنصة.<br>
        - يتم احتساب هذه النسبة تلقائيًا ضمن نظام الحجز.<br>
        - مواصلة استخدامك للموقع تعني موافقتك على هذه النسبة.
    </p>

    <h3>4. سياسة الإلغاء</h3>
    <p>
        - في حال رغبة العميل بإلغاء الحجز، يجب التواصل مع مالك القاعة مباشرة.<br>
        - الموقع غير مسؤول عن أي مبالغ مالية بين الطرفين.
    </p>

    <h3>5. حقوق الملكية</h3>
    <p>
        - جميع محتويات الموقع (تصاميم، نصوص، صور) مملوكة لإدارة الموقع.<br>
        - يمنع نسخ أو إعادة نشر أي جزء بدون إذن مسبق.
    </p>

    <h3>6. التعديلات</h3>
    <p>
        - يحق لإدارة الموقع تعديل الشروط والأحكام في أي وقت.<br>
        - استمرارك في استخدام الموقع بعد التعديل يعني موافقتك على الشروط الجديدة.
    </p>

